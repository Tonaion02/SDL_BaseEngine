//------------------------------------------------------------------------------------
//TileLayer Class
//------------------------------------------------------------------------------------
TileLayer::TileLayer(uint16_t width, uint16_t height)
{
	for (int i = 0; i < height; i++)
	{
		m_tiles.push_back(std::vector<CommonTile>(width));
		for (int j = 0; j < width; j++)
		{
			m_tiles[i][j] = CommonTile(0, 0);
		}
	}
}
