TileMap::TileMap(uint16_t width, uint16_t height)
	:m_width(width), m_height(height)
{
	for (int j = 0; j < height; j++)
	{
		for (int i = 0; i < width; i++)
		{
			m_tiles[j][i] = CommonTile(0, 0);
		}
	}
}



CommonTile& TileMap::getTile(int y, int x)
{
	return m_tiles[y][x];
}
